package helper;

public class priceTotal {

	private int totalPrice;
	
	

	public priceTotal() {
		super();
	}

	public priceTotal(int totalPrice) {
		super();
		this.totalPrice = totalPrice;
	}

	public int getTotalPrice() {
		return totalPrice;
	}

	public void setTotalPrice(int totalPrice) {
		this.totalPrice = totalPrice;
	}
	
	
}
